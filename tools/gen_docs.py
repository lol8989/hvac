FONT = "Malgun Gothic"


def set_ko_font(doc):
    for name in ["Normal", "Title", "Heading 1", "Heading 2", "Heading 3", "List Bullet"]:
        try:
            style = doc.styles[name]
        except KeyError:
            continue
        style.font.name = FONT
        rpr = style.element.get_or_add_rPr()
        rfonts = rpr.get_or_add_rFonts()
        rfonts.set(qn("w:eastAsia"), FONT)
        rfonts.set(qn("w:ascii"), FONT)
        rfonts.set(qn("w:hAnsi"), FONT)
    doc.styles["Normal"].font.size = Pt(10)


def h(doc, t, lv):
    return doc.add_heading(t, level=lv)


def para(doc, t, italic=False, size=10):
    p = doc.add_paragraph()
    r = p.add_run(t)
    r.italic = italic
    r.font.size = Pt(size)
    return p


def bullet(doc, t):
    doc.add_paragraph(t, style="List Bullet")


def pseudo(doc, lines):
    for ln in lines:
        p = doc.add_paragraph()
        p.paragraph_format.left_indent = Pt(14)
        p.paragraph_format.space_after = Pt(0)
        p.paragraph_format.space_before = Pt(0)
        p.add_run(ln if ln else " ").font.size = Pt(9)
        pPr = p._p.get_or_add_pPr()
        pPr.append(pPr.makeelement(qn("w:shd"), {qn("w:val"): "clear", qn("w:fill"): "F2F2F2"}))


def table(doc, headers, rows):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = "Table Grid"
    for i, htext in enumerate(headers):
        c = t.rows[0].cells[i]
        c.text = ""
        run = c.paragraphs[0].add_run(htext)
        run.bold = True
        run.font.size = Pt(9)
        tcPr = c._tc.get_or_add_tcPr()
        tcPr.append(tcPr.makeelement(qn("w:shd"), {qn("w:val"): "clear", qn("w:fill"): "DCDEE0"}))
    for row in rows:
        cells = t.add_row().cells
        for i, val in enumerate(row):
            cells[i].text = ""
            cells[i].paragraphs[0].add_run(str(val)).font.size = Pt(9)
    return t


