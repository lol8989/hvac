def build_wbs():
    doc = Document()
    set_ko_font(doc)
    doc.add_heading("", level=0).add_run("도면검도시스템 WBS · 지라 티켓 분류").font.size = Pt(20)
    doc.add_paragraph().add_run("개발 착수·지라 등록용 작업 분해 구조 (Epic → Story/Task) — 구현 독립 신규 백로그").italic = True
    doc.add_paragraph().add_run("버전 v2.0 · 2026-07-03 · 전 항목 상태: To Do(신규 백로그)").font.size = Pt(9)
    doc.add_paragraph()

    h(doc, "사용법 (지라 등록)", 1)
    bullet(doc, "구조: Epic(대분류) → Story(사용자가치)/Task(기술작업). 전 항목은 신규 백로그(상태 To Do)이다.")
    bullet(doc, "지라 import: 동봉된 '지라_import_wbs.csv'를 Jira 'Import from CSV'로 등록. Epic Link로 상위 Epic에 연결.")
    bullet(doc, "필드: 영역(프론트/백엔드/도메인/워커/공통)·우선순위(High/Medium/Low)·SP(스토리포인트)·완료조건(AC).")
    bullet(doc, "본 WBS는 특정 코드/구현이 아닌 '해야 할 작업'을 기술한다(구현 독립).")

    h(doc, "우선순위·스토리포인트 기준", 1)
    table(doc, ["항목", "기준"], [
        ["High", "핵심 파이프라인·차단 요소·기반"],
        ["Medium", "주요 기능이나 대체·후속 가능"],
        ["Low", "개선·편의·후순위"],
        ["SP(피보나치)", "1·2·3=소, 5=중, 8=대, 13=분할 권장"],
    ])

    total = sum(len(t) for _, _, t in WBS)
    h(doc, "요약", 1)
    para(doc, f"에픽 {len(WBS)}개 · 작업 {total}개 (전 항목 신규 백로그 · 상태 To Do).")

    for eid, ename, tasks in WBS:
        h(doc, f"{eid}. {ename}", 1)
        table(doc, ["ID", "유형", "요약", "영역", "우선", "SP", "완료조건(AC)"], [list(t) for t in tasks])

    out = os.path.join(ROOT, "도면검도시스템_WBS_지라티켓분류.docx")
    doc.save(out)
    return out


def build_csv():
    out = os.path.join(ROOT, "지라_import_wbs.csv")
    with open(out, "w", newline="", encoding="utf-8-sig") as f:
        w = csv.writer(f)
        w.writerow(["Issue Type", "Summary", "Epic Name", "Epic Link", "Component",
                    "Priority", "Story Points", "Status", "Labels", "Description"])
        for eid, ename, tasks in WBS:
            w.writerow(["Epic", f"[{eid}] {ename}", ename, "", "", "High", "", "To Do", eid.lower(), f"에픽: {ename}"])
            for (tid, ttype, summ, area, prio, sp, ac) in tasks:
                w.writerow([ttype, f"[{tid}] {summ}", "", ename, area, prio, sp, "To Do", area, f"완료조건(AC): {ac}"])
    return out


